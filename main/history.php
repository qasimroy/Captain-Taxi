                <h2 style="letter-spacing: 3px; font-weight: 500;">When and How ?</h2>
                <p>It all started 3 years ago in 2019 when former cab drivers starting new Saskatoon taxi company.
                     Captain Taxi aimed at breaking monopoly, taking part in city hall's new enterprise licence program</p>
                <p>The advent of ride sharing in Saskatoon makes this a most unlikely time to start a new cab company.
                    Article content
                    Yet the potential effect of ride sharing on the taxi industry has failed to deter two former Saskatoon cabbies from starting a new company called Captain Taxi Ltd. The venture is expected to launch on Aug. 8 and is aimed at both breaking the taxi monopoly and taking advantage of recent changes to the taxi bylaw.</p>
                <p>“We are here to help the drivers,” Zuhaib Jahangir said in an interview. “We think a happy cab driver means a happy customer.”</p>
                <p>Jahangir, who is originally from Pakistan, and Mark Gill, a former Saskatoon driver who was born in India, have teamed up to start the new company.</p>
                <p>“There is a monopoly right now.”</p>
                <p>Saskatoon’s two most prominent taxi companies, the United Group and Comfort Cabs, announced they were merging a year ago.</p>
                <p>In April, city council unanimously passed a change to the city’s taxi bylaw that eliminated the seasonal taxi licences for a new enterprise licence system.</p>
                <p>Jahangir said Captain Taxi will welcome new drivers under the enterprise licence program. The program introduces 35 new licences that can be used 14 hours a day, six days a week, but by only a single driver. The licences are valid for four years and will be awarded by lottery, but only to cab drivers with at least four years of experience.</p>    
                <p>“We want to encourage everybody, all of those who are eligible, to put their names in the draw,” Jahangir said.</p>
                <p>
                    He has been told by some drivers that they have been warned by the United Group that if they leave for the new startup, they will not be welcomed back, he said, adding he has also heard drivers have been told by United not to take part in the enterprise licence lottery.
                </p>
                <p>Jahangir said the new company has commitments from three drivers and expects to have 10 when it starts operations next month.
                </p>
                <p>Malik Umar Draz, president of the United Steel Workers Local 2014, which represents more than 400 drivers, said he welcomes the new company.</p>
                <p>“I’m happy to see that there’s more competition because monopoly’s not good for any business,” Draz said in an interview.</p>
                <p>Jahangir said the cab company’s vehicles will start out as both black and white, but will eventually be black. A logo has been designed and is ready for the Aug. 8 debut.</p>
                <p>He believes there’s room for both a new taxi company and ride sharing, he said. Ride-sharing giant Uber started operating in early February after new regulations to accommodate ride sharing were introduced in December.</p>
                <p>According to city hall, three more ride-sharing services have started operations in Saskatoon: RideX, Riide-hail and Rel8Well.</p>
                <p>“We want the drivers to feel less stress, more happy,” Jahangir said. “We want to keep the dollars in our economy, in Saskatoon.”</p>
            
